function formatBgJargonField(data) {
  const limitedDefinitions = data.definitions.slice(0, 3);

  return limitedDefinitions
    .map((def, index) => {
      let text = `**${index + 1}.** ${def.meaning}`;

      if (def.example) {
        text += `\n*"${def.example}"*`;
      }

      text += `\n-# ğŸ‘ ${def.votesYes}  ğŸ‘ ${def.votesNo}\n\u200E\n`;

      return text;
    })
    .join("\n\n");
}

